package Basics;

public class Elseif {
	public static void main(String[] args) 
    {
        int x=21;
        if(x>=1&&x<=5)
        {System.out.println("no. is between 1to 5");}
        else if(x>=6&&x<=10)
        {System.out.println("no. is between 6 to 10");}
        else if(x>=11&&x<=15)
        {System.out.println("no. is between 11 to 15");}
        else if(x>=16&&x<=20)
        {System.out.println("no. is between 16 to 20");}
        else
        {System.out.println("no. is larger than 20");}
    }
 
}
